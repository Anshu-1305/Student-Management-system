export const getSemesterSummary = (semesterKey) => {
  const data = syllabusData[semesterKey];
  if (!data) return null;

  const summary = {
    core: data.subjects.filter(s => s.type === "core"),
    electives: data.subjects.filter(s => s.type === "elective"),
    labs: data.subjects.filter(s => s.type === "lab"),
    projects: data.subjects.filter(s => s.type === "project"),
    mandatory: data.subjects.filter(s => s.type === "mandatory")
  };

  return {
    ...summary,
    totalSubjects: data.subjects.length,
    totalCredits: data.totalCredits,
    creditDistribution: {
      core: summary.core.reduce((sum, s) => sum + s.credits, 0),
      electives: summary.electives.reduce((sum, s) => sum + s.credits, 0),
      labs: summary.labs.reduce((sum, s) => sum + s.credits, 0),
      projects: summary.projects.reduce((sum, s) => sum + s.credits, 0),
      mandatory: summary.mandatory.reduce((sum, s) => sum + s.credits, 0)
    }
  };
};